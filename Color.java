public enum Color {    
  RED(255, 0, 0, "red"),
  GREEN(0, 255, 0, "green"),
  YELLOW(255, 200, 0, "yellow"),
  BLUE(0, 0, 255, "blue"),
  PURPLE(200, 0, 255, "purple"),
  GREY(50, 50, 50, "grey");

  private int r;
  private int g;
  private int b;
  private String name;

  private Color(int rr, int gg, int bb, String n) {
    r = rr;
    g = gg;
    b = bb;        
    name = n;
  }

  public int[] getRGB() {
    int[] rgb = {
      r, g, b
    };
    return rgb;
  }  
  
  public String getName() {
    return name;
  }
} 

